    private void tabel_tugasKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tabel_tugasKeyPressed
        if (evt.getKeyCode()==KeyEvent.VK_DOWN || evt.getKeyCode()==KeyEvent.VK_UP);
        try{
            int row =tabel_tugas.getSelectedRow();
            String Table_click=(tabel_tugas.getModel().getValueAt(row, 0).toString());
            String sql="select * from tugas where kd_tgs='"+Table_click+"'";
            pst=conn.prepareStatement(sql);
        rs=pst.executeQuery();
        if(rs.next()){
            String add1 =rs.getString("kd_tgs");
            txt_kode.setText(add1);
            String add2 =rs.getString("nm_tgs");
            txt_nama.setText(add2);
            String add3 =rs.getString("ket_tgs");
            txt_ket.setText(add3);
            String add4 =rs.getString("prior");
            combo.setSelectedItem(add4);
            String add5 =rs.getString("status");
            txt_status.setText(add5);
                        
        }
        } catch (Exception e){
          JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tabel_tugasKeyPressed

    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        try{
            String sql ="insert into tugas (kd_tgs,nm_tgs,ket_tgs,prior,status) values (?,?,?,?,?)";
            pst=conn.prepareStatement(sql);
            pst.setString(1, txt_kode.getText());
            pst.setString(2, txt_nama.getText());
            pst.setString(3, txt_ket.getText());
            pst.setString(4, combo.getSelectedItem().toString());
            pst.setString(5, txt_status.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Tersimpan");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        clear();
        Update_table();
    }//GEN-LAST:event_saveActionPerformed

