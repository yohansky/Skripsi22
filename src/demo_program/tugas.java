        private void datatable(){
        Object[] Baris = {"Kode Tugas","Nama Tugas","Keterangan Tugas","Prioritas","Status"};
        tabmode = new DefaultTableModel(null, Baris);
        tabel_tugas.setModel(tabmode);
        String sql = "select * from tugas";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String a = hasil.getString("kd_tgs");
                String b = hasil.getString("nm_tgs");
                String c = hasil.getString("ket_tgs");
                String d = hasil.getString("prior");
                String e = hasil.getString("status");
                                
                String[] data = {a,b,c,d,e};
                tabmode.addRow(data);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

