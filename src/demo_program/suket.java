    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        int row =tabel_sk.getSelectedRow();
        int p = JOptionPane.showConfirmDialog(null,"Apakah Anda Yakin?","Terhapus",JOptionPane.YES_NO_OPTION);
        if(p==0){
            String Table_click=(tabel_sk.getModel().getValueAt(row, 0).toString());
            String sql="delete from sk where no_sk='"+Table_click+"'";
            try {
                pst=conn.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Terhapus");

            }catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
            clear();
            Update_table();
        }
    }//GEN-LAST:event_hapusActionPerformed

    private void tabel_skKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tabel_skKeyPressed
        if (evt.getKeyCode()==KeyEvent.VK_DOWN || evt.getKeyCode()==KeyEvent.VK_UP);
        try{
            int row =tabel_sk.getSelectedRow();
            String Table_click=(tabel_sk.getModel().getValueAt(row, 0).toString());
            String sql="select * from sk where no_sk='"+Table_click+"'";
            pst=conn.prepareStatement(sql);
        rs=pst.executeQuery();
        if(rs.next()){
            String add1 =rs.getString("no_sk");
            txt_kodes.setText(add1);
            String add2 =rs.getString("no_bp");
            txt_kodeb.setText(add2);
            String add3 =rs.getString("nip");
            txt_nip.setText(add3);
            String add4 =rs.getString("nm_peg");
            txt_namap.setText(add4);
            String add5 =rs.getString("kd_tgs");
            txt_kodet.setText(add5);
            String add6 =rs.getString("nm_tgs");
            txt_namat.setText(add6);
            String add7 =rs.getString("tenggat");
            txt_date.setDateFormatString(add7);
                        
        }
        } catch (Exception e){
          JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_tabel_skKeyPressed

