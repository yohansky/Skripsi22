    private void datatable(){
        Object[] Baris = {"Nomor SK","Nomor Bukti","Nomor Induk Pegawai","Nama Pegawai","Kode Tugas","Nama Tugas","Tenggat"};
        tabmode = new DefaultTableModel(null, Baris);
        tabel_sk.setModel(tabmode);
        String sql = "select * from sk";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String a = hasil.getString("no_sk");
                String b = hasil.getString("no_bp");
                String c = hasil.getString("nip");
                String d = hasil.getString("nm_peg");
                String e = hasil.getString("kd_tgs");
                String f = hasil.getString("nm_tgs");
                String g = hasil.getString("tenggat");
                               
                String[] data = {a,b,c,d,e,f,g};
                tabmode.addRow(data);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

