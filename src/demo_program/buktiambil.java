    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        try{
            String sql ="insert into buktiambil (no_bp,tgl,nip,nm_peg,divisi,kd_tgs,nm_tgs,ket_tgs,prior) values (?,?,?,?,?,?,?,?,?)";
            pst=conn.prepareStatement(sql);
            pst.setString(1, txt_nob.getText());
            pst.setString(2, ((JTextField)txt_date.getDateEditor().getUiComponent()).getText());
            pst.setString(3, txt_nip.getText());
            pst.setString(4, txt_namap.getText());
            pst.setString(5, txt_divisi.getText());
            pst.setString(6, txt_kodet.getText());
            pst.setString(7, txt_namat.getText());
            pst.setString(8, txt_ket.getText());
            pst.setString(9, txt_prior.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Tersimpan");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        clear();
        Update_table();
    }//GEN-LAST:event_saveActionPerformed

