    private void datatable(){
        Object[] Baris = {"Nomor Bukti","Tanggal","Nomor Induk Pegawai","Nama Pegawai","Divisi","Kode Tugas","Nama Tugas","Keterangan","Prioritas"};
        tabmode = new DefaultTableModel(null, Baris);
        tabel_bukti.setModel(tabmode);
        String sql = "select * from buktiambil";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String a = hasil.getString("no_bp");
                String b = hasil.getString("tgl");
                String c = hasil.getString("nip");
                String d = hasil.getString("nm_peg");
                String e = hasil.getString("divisi");
                String f = hasil.getString("kd_tgs");
                String g = hasil.getString("nm_tgs");
                String h = hasil.getString("ket_tgs");
                String i = hasil.getString("prior");
                                
                String[] data = {a,b,c,d,e,f,g,h,i};
                tabmode.addRow(data);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

