    private void datatable(){
        Object[] Baris = {"Nomor Induk Pegawai","Nama Pegawai","Tanggal Lahir","Jenis Kelamin","Nomor HP","Divisi","Alamat",};
        tabmode = new DefaultTableModel(null, Baris);
        tabel_pegawai.setModel(tabmode);
        String sql = "select * from pegawai";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String a = hasil.getString("nip");
                String b = hasil.getString("nm_peg");
                String c = hasil.getString("tgl");
                String d = hasil.getString("jk");
                String e = hasil.getString("no_hp");
                String f = hasil.getString("divisi");
                String g = hasil.getString("alamat");
                
                String[] data = {a,b,c,d,e,f,g};
                tabmode.addRow(data);
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
        

